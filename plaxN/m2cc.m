%**********************************************************************
function [C] = m2cc(m,s);

C = zeros(s,1);

if s==9
  C = [m(1,1); m(2,2); m(3,3);
       m(1,2); m(2,1); m(2,3); m(3,2); m(3,1); m(1,3)];
elseif s==5
  C = [m(1,1); m(2,2); m(3,3); m(1,2); m(2,1)];
elseif s==4
  C = [m(1,1); m(2,2); m(1,2); m(2,1)];
end;
